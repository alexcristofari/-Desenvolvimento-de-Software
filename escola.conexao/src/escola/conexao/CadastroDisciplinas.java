     private void carregarProfessores() {
        ProfessorDAO pDAO = new ProfessorDAO();
        List<Professor> professores = pDAO.listarProfessores();
        
        for (Professor p : professores) {
            jComboBoxProfessor.addItem(p);
        }
    }
