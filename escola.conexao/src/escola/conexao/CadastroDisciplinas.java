    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nomeDisciplina = txtNomeDisciplina.getText();
        int cargaHoraria = Integer.parseInt(txtCargaHoraria.getText());
        Professor professorSelecionado = (Professor) jComboBoxProfessor.getSelectedItem();

        Disciplina disciplina = new Disciplina();
        disciplina.setNome(nomeDisciplina);
        disciplina.setCargaHoraria(cargaHoraria);
        disciplina.setProfessorId(professorSelecionado.getId_professor());

        DisciplinaDAO dDAO = new DisciplinaDAO();
        dDAO.inserir(disciplina);
        
        JOptionPane.showMessageDialog(this, "Disciplina cadastrada com sucesso!");
        limparFormulario();
    }//GEN-LAST:event_btnSalvarActionPerformed
     private void limparFormulario() {
        txtNomeDisciplina.setText("");
        txtCargaHoraria.setText("");
        jComboBoxProfessor.setSelectedIndex(0);
    }
